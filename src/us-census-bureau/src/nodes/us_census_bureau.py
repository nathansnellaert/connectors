"""US Census Bureau connector — implement step (downloads + transforms).

One NodeSpec per collect entity (1758 datasets across ACS aggregate, Decennial,
Population Estimates/Projections, Economic Census, business surveys, CPS/SIPP
microdata, and EITS/trade timeseries). All are fetched through the canonical
Census Data API (api.census.gov/data) chosen by research.

Per dataset we read the key-free variables.json + geography.json metadata, then
issue the actual `get=<vars>&for=<geo>:*` data query (the only part needing the
free CENSUS_API_KEY). Because the corpus spans four query regimes with wildly
different geography/required-predicate shapes, fetch_one is an ADAPTIVE builder:
it constructs an ordered list of query variants (broadest no-parent geography
first, then parent-`in` clauses, then no-`for`; `time=` added when required) and
commits to the FIRST variant that returns >=1 data row, streaming that response
to NDJSON.gz with a row cap so memory/size stay bounded on large microdata and
no-geography trade timeseries.

Stateless full re-pull: each dataset is one (bounded) query per refresh; no
watermark is needed and the source exposes no general incremental filter.

Raw format: NDJSON — Census responses are arrays-of-arrays of all-string cells
with per-dataset column sets (heterogeneous across the 1758 datasets), so a
single declared parquet schema is impossible; NDJSON re-types on read.

Transform: SELECT * passthrough. With 1758 distinct, undocumented column sets a
blind cast would be brittle; the transform is a thin publish gate (0 rows fails
the node) and DuckDB reads the NDJSON columns as-is.
"""
