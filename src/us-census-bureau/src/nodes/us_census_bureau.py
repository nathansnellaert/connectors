# --- tuning knobs --------------------------------------------------------
MAX_GET = 40            # <=50 Census per-call variable cap (predicates excluded)
MAX_ROWS = 1_000_000    # safety bound for huge microdata / commodity timeseries
GEO_PREF = ("state", "us", "region", "division", "place", "county")
TIME_RANGES = ("from 2000", "from 1980")  # widest first; maximizes >=1-row hit
# Concrete parent codes for geographies whose parent is not wildcardable
# (state 01 = Alabama, county 001 = Autauga — guaranteed to exist).
CONCRETE_PARENT = {"state": "01", "county": "001"}

_TRANSIENT_EXC = (
    httpx.ConnectError, httpx.ConnectTimeout, httpx.ReadTimeout,
    httpx.WriteTimeout, httpx.PoolTimeout, httpx.RemoteProtocolError,
    httpx.ProxyError,
)


def _is_transient(exc: BaseException) -> bool:
    if isinstance(exc, _TRANSIENT_EXC):
        return True
    if isinstance(exc, httpx.HTTPStatusError):
        code = exc.response.status_code
        return code == 429 or 500 <= code < 600
    return False


class _Reader:
    """Minimal file-like adapter over httpx iter_bytes() for ijson streaming."""

    def __init__(self, byte_iter):
        self._it = byte_iter
        self._buf = b""

    def read(self, n=-1):
        if n is None or n < 0:
            chunks = [self._buf]
            chunks.extend(self._it)
            self._buf = b""
            return b"".join(chunks)
        while len(self._buf) < n:
            try:
                self._buf += next(self._it)
            except StopIteration:
                break
        out, self._buf = self._buf[:n], self._buf[n:]
        return out


@retry(
    retry=retry_if_exception(_is_transient),
    stop=stop_after_attempt(6),
    wait=wait_exponential(min=4, max=120),
    reraise=True,
)
def _get_json(url: str):
    resp = get(url, timeout=(10.0, 120.0))
    resp.raise_for_status()
    return resp.json()


def _select_get_vars(vmeta: dict) -> list:
    """Pick up to MAX_GET non-predicate variables; always keep required ones."""
    items = list(vmeta.items())
    required_get = [
        k for k, v in items
        if str(v.get("required")).lower() == "true" and not v.get("predicateOnly")
    ]
    others = [
        k for k, v in items
        if not v.get("predicateOnly") and k not in required_get
    ]
    if "NAME" in others:
        others.remove("NAME")
        others.insert(0, "NAME")
    get_vars = required_get[:MAX_GET]
    room = MAX_GET - len(get_vars)
    if room > 0:
        get_vars += others[:room]
    seen, out = set(), []
    for v in get_vars:
        if v not in seen:
            seen.add(v)
            out.append(v)
    return out


def _build_in(parents: list, wildcard) -> str | None:
    """Build a Census `in=` value: wildcard where allowed, concrete code else."""
    wc = set(wildcard or [])
    parts = []
    for p in parents:
        if p in wc:
            parts.append(f"{p}:*")
        elif p in CONCRETE_PARENT:
            parts.append(f"{p}:{CONCRETE_PARENT[p]}")
        else:
            return None
    return " ".join(parts)


def _geo_clauses(geos: list) -> list:
    """Ordered geography clauses: no-parent levels first, then parent-`in`."""
    norq = [g for g in geos if not g.get("requires")]
    ordered = []
    for p in GEO_PREF:
        for g in norq:
            if g.get("name") == p and g not in ordered:
                ordered.append(g)
    for g in norq:
        if g not in ordered:
            ordered.append(g)
    clauses = [{"for": f"{g['name']}:*"} for g in ordered]
    for g in geos:
        reqs = g.get("requires") or []
        if not reqs:
            continue
        in_val = _build_in(reqs, g.get("wildcard"))
        if in_val is None:
            continue
        clauses.append({"for": f"{g['name']}:*", "in": in_val})
    return clauses


def _stream_variant(base: str, params: dict, asset: str) -> bool:
    """Try one query variant. Returns True if it returned data and we wrote it,
    False if the variant produced no data (try the next one). Transient network
    errors propagate so the retried caller re-runs."""
    client = get_client()
    with client.stream("GET", base, params=params, timeout=(15.0, 300.0)) as resp:
        if resp.status_code != 200:
            return False
        if "json" not in resp.headers.get("content-type", "").lower():
            return False  # missing-key HTML or other non-data body
        gen = ijson.items(_Reader(resp.iter_bytes()), "item")
        try:
            header = next(gen)
        except StopIteration:
            return False
        if not isinstance(header, list):
            return False
        try:
            first = next(gen)
        except StopIteration:
            return False  # header only -> no data rows
        n = 0
        with raw_writer(asset, "ndjson.gz", mode="wt",
                        compression="gzip", encoding="utf-8") as w:
            w.write(json.dumps(dict(zip(header, first))) + "\n")
            n += 1
            for row in gen:
                if n >= MAX_ROWS:
                    print(f"{asset}: hit MAX_ROWS={MAX_ROWS}, truncating", flush=True)
                    break
                w.write(json.dumps(dict(zip(header, row))) + "\n")
                n += 1
        print(f"{asset}: wrote {n} rows via {params.get('for', '(no for)')}",
              flush=True)
        return True


@retry(
    retry=retry_if_exception(_is_transient),
    stop=stop_after_attempt(5),
    wait=wait_exponential(min=4, max=120),
    reraise=True,
)
def _fetch_core(asset: str, base: str, key: str) -> None:
    vmeta = _get_json(base + "/variables.json").get("variables", {}) or {}
    geos = _get_json(base + "/geography.json").get("fips", []) or []
    get_vars = _select_get_vars(vmeta)
    if not get_vars:
        raise RuntimeError(f"{asset}: no non-predicate variables to fetch")
    needs_time = any(
        k == "time" and v.get("predicateOnly")
        and str(v.get("required")).lower() == "true"
        for k, v in vmeta.items()
    )
    get_param = ",".join(get_vars)
    time_opts = TIME_RANGES if needs_time else (None,)
    geo_clauses = _geo_clauses(geos) + [{}]  # no-`for` fallback last

    attempts = 0
    for gc in geo_clauses:
        for t in time_opts:
            params = {"get": get_param, "key": key}
            params.update(gc)
            if t:
                params["time"] = t
            attempts += 1
            if _stream_variant(base, params, asset):
                return
    raise RuntimeError(
        f"{asset}: no query variant returned data ({attempts} tried)"
    )


def fetch_one(node_id: str) -> None:
    asset = node_id  # the runtime passes the spec id; it IS the asset name
    key = os.environ.get("CENSUS_API_KEY")
    if not key:
        raise RuntimeError(
            "CENSUS_API_KEY env var is required for Census Data API queries"
        )
    base = BASE_URLS[asset]
    _fetch_core(asset, base, key)


